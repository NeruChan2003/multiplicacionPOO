// se declara la clase
public class Multiplicacion {
    // se declaran las variables
    int a, b, r;

    // se declara el metodo
    public int multiplicar(int a, int b) {
        // se llama al objeto
        r = this.a * this.b;

        return r;
        // se cierra el metodo dividir
    }
    // se cierra la clase
}
